fun main()
{
    println("Write number:")
    var x = readLine()!!.toInt()
    when(x)
    {
        0 -> println("Zero")
        1 -> println("One")
        2 -> println("Two")
        3 -> println("Three")
        4 -> println("Four")
        5 -> println("Five")
        6 -> println("Six")
        7 -> println("Seven")
        8 -> println("Eight")
        9 -> println("Nine")
        10 -> println("Ten")
    }
}